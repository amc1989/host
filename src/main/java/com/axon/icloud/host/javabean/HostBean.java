package com.axon.icloud.host.javabean;

public class HostBean {
	private String phone;
	private String idate;
	private String subID;
	private String rBytes;
	private String num;
	private String dayNum;
	private String tagName;
	private String score;
    private String useNum;
    private String hostID;
	public String getHostID() {
		return hostID;
	}

	public void setHostID(String hostID) {
		this.hostID = hostID;
	}

	public String getUseNum() {
		return useNum;
	}

	public void setUseNum(String useNum) {
		this.useNum = useNum;
	}

	public String getTagName() {
		return tagName;
	}

	public String getScore() {
		return score;
	}

	public void setScore(String score) {
		this.score = score;
	}

	public void setTagName(String tagName) {
		this.tagName = tagName;
	}

	public HostBean() {
	}

	public String getPhone() {
		return phone;
	}

	public void setPhone(String phone) {
		this.phone = phone;
	}

	public String getIdate() {
		return idate;
	}

	public void setIdate(String idate) {
		this.idate = idate;
	}

	

	public String getSubID() {
		return subID;
	}

	public void setSubID(String subID) {
		this.subID = subID;
	}

	public String getrBytes() {
		return rBytes;
	}

	public void setrBytes(String rBytes) {
		this.rBytes = rBytes;
	}

	public String getNum() {
		return num;
	}

	public void setNum(String num) {
		this.num = num;
	}

	public String getDayNum() {
		return dayNum;
	}

	public void setDayNum(String dayNum) {
		this.dayNum = dayNum;
	}

	@Override
	public String toString() {
		return "HostBean [phone=" + phone + ", idate=" + idate + ", subID="
				+ subID + ", rBytes=" + rBytes + ", num=" + num + ", dayNum="
				+ dayNum + ", tagName=" + tagName + ", score=" + score
				+ ", useNum=" + useNum + "]";
	}

}
